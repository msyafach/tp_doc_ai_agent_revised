# ─── Node: Company Location Analysis (Sec 5.1.2.1) ───────────────────────────

def research_company_location(state: dict) -> dict:
    """
    Research economic/business efficiency factors for the company's location.

    Covers Inflation Rate, Economic Growth, and Exchange Rate using Tavily
    web search, then generates structured Section 5.1.2.1 content.

    Returns:
        company_location_analysis: str  — formatted paragraphs
        company_location_sources:  list — source URLs
    """
    company_address = state.get("company_address", "")
    fiscal_year     = state.get("fiscal_year", "2024")
    products        = state.get("products", [])
    product_names   = ", ".join(p.get("name", "") for p in products if p.get("name")) or "general"

    # Extract city/province from address (use first meaningful token)
    _location_tokens = [t.strip() for t in company_address.replace(",", " ").split() if len(t.strip()) > 3]
    location = " ".join(_location_tokens[:3]) if _location_tokens else "Indonesia"

    # ── Tavily searches ──────────────────────────────────────────────────────
    raw_inflation = search_web(
        f"Indonesia inflation rate CPI {fiscal_year} Bank Indonesia BPS data",
        max_results=4,
    )
    raw_gdp = search_web(
        f"Indonesia economic growth GDP {fiscal_year} quarter BPS statistics",
        max_results=4,
    )
    raw_fx = search_web(
        f"Indonesia Rupiah IDR exchange rate USD {fiscal_year} Bank Indonesia",
        max_results=3,
    )

    results_inflation = sanitize_search_results(raw_inflation.get("results", []), max_results=4)
    results_gdp       = sanitize_search_results(raw_gdp.get("results",  []), max_results=4)
    results_fx        = sanitize_search_results(raw_fx.get("results",   []), max_results=3)

    tavily_inf = raw_inflation.get("answer", "N/A")[:400]
    tavily_gdp = raw_gdp.get("answer", "N/A")[:400]

    # Collect unique sources
    all_results = results_inflation + results_gdp + results_fx
    sources = list(dict.fromkeys(r.get("url", "") for r in all_results if r.get("url")))

    prompt = f"""You are a transfer pricing analyst writing Section 5.1.2.1 "Efficiency and Excellence Levels of Company Locations" of a Transfer Pricing Local File document.

Company context:
- Company location / address: {company_address or location}
- Products/services: {product_names}
- Fiscal year: {fiscal_year}

{UNTRUSTED_DATA_NOTICE}

=== Research: Inflation Rate ===
{json.dumps(results_inflation, indent=2)[:1800]}
Tavily summary: {tavily_inf}

=== Research: Economic Growth / GDP ===
{json.dumps(results_gdp, indent=2)[:1800]}
Tavily summary: {tavily_gdp}

=== Research: Exchange Rate ===
{json.dumps(results_fx, indent=2)[:1200]}

Write a professional section covering the economic efficiency factors that affect business performance in the company's location in Indonesia. Structure the content as follows, using the EXACT pattern:

**Inflation Rate;**
[1–2 paragraphs with specific CPI figures, y-o-y change, BI target range, trend outlook. Cite footnotes like [1], [2].]

**Economic Growth;**
[1–2 paragraphs with GDP q-t-q / y-t-y / c-t-c growth figures, province contribution, outlook. Cite footnotes.]

**Exchange Rate/Exchange Rate;**
[1–2 paragraphs with IDR/USD bid rate, SBN yield, DXY movement, trend. Cite footnotes.]

IMPORTANT RULES:
- RESPOND IN ENGLISH ONLY.
- Use ONLY factual data from the search results. Do NOT invent numbers.
- Include a brief intro sentence before the first bullet: "There are several factors that affect business performance in the [industry] industry in [location], as follows:"
- Use **bold label;** followed by a newline for each factor heading.
- Include source citations [N] inline where data is referenced.
- Formal, professional English suitable for a tax compliance document.
- Do NOT add any section heading — start directly with the intro sentence.
- Do NOT append a "Sources", "Footnotes", or reference-summary section at the end.
"""
    response_text = _strip_source_summary(invoke_prompt_with_tools(prompt))
    return {
        "company_location_analysis": response_text,
        "company_location_sources":  sources,
    }


