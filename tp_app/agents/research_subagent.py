"""
Research Subagent — web-research nodes for the TP documentation workflow.

Nodes:
  - research_industry_global      → industry_analysis_global, industry_global_sources
  - research_industry_indonesia   → industry_analysis_indonesia, industry_indonesia_sources
  - research_business_environment → business_environment_overview
  - research_company_location     → company_location_analysis, company_location_sources
  - research_industry_regulations → industry_regulations_text, industry_regulations_sources
"""
import json
import re
from agents.llm_factory import invoke_prompt_with_tools
from agents.tools import UNTRUSTED_DATA_NOTICE, sanitize_search_results, search_web


def _strip_source_summary(text: str) -> str:
    """
    Remove model-added source summary blocks such as
    "Sources (footnotes): [1] ... [2] ..." from section bodies.
    Source URLs are rendered separately by the exporter.
    """
    cleaned = re.sub(
        r"\n+\s*(sources?\s*(\([^)]*\))?\s*:|footnotes?\s*:).*$",
        "",
        text,
        flags=re.IGNORECASE | re.DOTALL,
    )
    return cleaned.strip()


# ─── Node: Industry Analysis (Global) ────────────────────────────────────────

def research_industry_global(state: dict) -> dict:
    """Research global industry analysis based on company's products/sector."""
    products = state.get("products", [])
    product_names = ", ".join([p.get("name", "") for p in products]) if products else "heavy equipment"
    business_desc = state.get("business_activities_description", "")
    industry_hint = product_names or "heavy equipment, construction machinery"

    raw1 = search_web(
        f"global {industry_hint} industry market size 2024 2025 growth CAGR forecast",
        max_results=5,
    )
    raw2 = search_web(
        f"global {industry_hint} market major players companies competition",
        max_results=3,
    )

    results1 = sanitize_search_results(raw1.get("results", []), max_results=5)
    results2 = sanitize_search_results(raw2.get("results", []), max_results=3)
    # Tavily's own summary is generated by Tavily's LLM — still sanitize for safety
    tavily_summary = raw1.get("answer", "N/A")[:500]

    prompt = f"""You are a transfer pricing analyst writing the Global Industry Analysis section of a Transfer Pricing Local File document for an Indonesian company.

Company context:
- Products/services: {product_names}
- Business description: {business_desc[:500]}

{UNTRUSTED_DATA_NOTICE}
Web research results on global industry:
{json.dumps(results1, indent=2)[:2500]}

Additional research on competition:
{json.dumps(results2, indent=2)[:1500]}

Tavily's summary answer: {tavily_summary}

Write a comprehensive Global Industry Analysis section (3-5 paragraphs) covering:
1. Definition and scope of the industry
2. Global market size (with specific USD figures and CAGR if available)
3. Key growth drivers and trends
4. Regional dynamics (especially Asia-Pacific)
5. Major global players/competitors

IMPORTANT RULES:
- RESPOND IN ENGLISH ONLY. Do NOT switch to Indonesian or any other language.
- Use ONLY factual data from the search results. Do NOT invent statistics.
- If a specific number is not available in the research, state the trend qualitatively instead.
- Cite sources where possible (e.g., "According to [source]...")
- Write in formal, professional English suitable for a tax compliance document.
- Do NOT include section headers — just flowing paragraphs.
- Include footnote markers like [1], [2] for source references.
- Do NOT append a "Sources", "Footnotes", or reference-summary section at the end.
"""
    response_text = _strip_source_summary(invoke_prompt_with_tools(prompt))
    sources = [r.get("url", "") for r in results1]

    return {
        "industry_analysis_global": response_text,
        "industry_global_sources": sources,
    }


