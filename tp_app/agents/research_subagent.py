# ─── Node: Comparable Companies Research ──────────────────────────────────────

def research_comparable_companies(state: dict) -> dict:
    """
    Research each comparable company using Tavily and generate a business
    description paragraph for Appendix 3.

    Returns:
        comparable_descriptions: dict  — {company_name: ai_description}
    """
    companies = [
        c for c in state.get("comparable_companies", []) if c.get("name")
    ]
    if not companies:
        return {"comparable_descriptions": {}}

    descriptions = {}

    for company in companies:
        name    = company.get("name", "")
        country = company.get("country", "")

        raw = search_web(
            f"{name} company business activities overview {country}",
            max_results=3,
        )
        results = sanitize_search_results(raw.get("results", []), max_results=3)
        tavily_answer = raw.get("answer", "")[:400]

        prompt = f"""You are a transfer pricing analyst writing Appendix 3 "Overview of Comparable Companies" in a Transfer Pricing Local File.

Write a concise business description (3–5 sentences) for the following company to be used as a comparable company entry:

Company name: {name}
Country: {country or "not specified"}

{UNTRUSTED_DATA_NOTICE}
Web research results:
{json.dumps(results, indent=2)[:2000]}
Tavily summary: {tavily_answer}

Rules:
- Describe the company's main business activities, products/services, and markets served
- Write in formal transfer pricing documentation English
- RESPOND IN ENGLISH ONLY
- Do NOT invent figures or facts not supported by the research
- No bullet points — write plain flowing sentences only
- Do NOT include the company name as a heading"""

        try:
            text = invoke_prompt_with_tools(prompt).strip()
        except Exception:
            text = f"{name} is a company operating in {country or 'its home country'}."

        descriptions[name] = text

    return {"comparable_descriptions": descriptions}
